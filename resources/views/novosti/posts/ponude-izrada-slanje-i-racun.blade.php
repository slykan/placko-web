<p>
    Prije nego što klijent dobije račun, obično dobije <strong>ponudu</strong>. U plačko.app modul
    Ponude sad pokriva cijeli taj put — od izrade i slanja, do pretvaranja prihvaćene ponude u
    gotov račun, bez ponovnog prepisivanja podataka.
</p>

<h2>1. Izrada ponude u par klikova</h2>
<p>
    Ponuda se otvara na isti način kao i račun — odabereš kupca iz popisa klijenata, a broj ponude
    se generira automatski po godišnjoj numeraciji (npr. <em>3-1-2026</em>). Uz to podešavaš:
</p>
<ul>
    <li><strong>Datum, vrijeme i mjesto izdavanja</strong> — mjesto se automatski predlaže iz podataka tvrtke.</li>
    <li><strong>Valjanost ponude</strong> u danima (npr. 30 dana), tako da klijent zna do kad vrijedi cijena.</li>
    <li><strong>Rok ispostave</strong> — kad će usluga ili proizvod biti isporučen (npr. "15 radnih dana").</li>
    <li><strong>Napomenu</strong>, unaprijed popunjenu iz postavki tvrtke, ali uvijek izmjenjivu.</li>
</ul>
<p>
    Stavke ponude mogu se birati izravno iz kataloga usluga/proizvoda (cijena i PDV stopa se
    povuku automatski) ili unijeti ručno. Za svaku stavku posebno se podešava količina, jedinica
    mjere, rabat u postotku i PDV stopa — a ukupan iznos, osnovica, rabat i PDV izračunavaju se
    uživo dok upisuješ. Stavke je moguće <strong>premještati redoslijedom</strong> i
    <strong>duplicirati</strong>, što je zgodno kod ponuda s puno sličnih pozicija.
</p>
<p>
    Gotova ponuda preuzima se kao uredan <strong>PDF</strong>, spreman za slanje klijentu.
</p>

<h2>2. Slanje ponude e-mailom, izravno iz aplikacije</h2>
<p>
    Ponudu više ne treba ručno preuzimati i lijepiti u mail — u popisu ponuda dovoljan je jedan
    klik na gumb <strong>Pošalji</strong>. Aplikacija:
</p>
<ul>
    <li>automatski generira PDF ponude i prilaže ga kao privitak,</li>
    <li>ponudi gotov predložak poruke s podacima klijenta, brojem ponude, datumom, valjanošću i ukupnim iznosom (predložak je slobodno moguće izmijeniti prije slanja),</li>
    <li>dopušta unos primatelja, kopije (CC) i dodatnih privitaka (npr. troškovnik ili katalog),</li>
    <li>šalje mail preko SMTP postavki tvrtke, ako su podešene — klijent tako dobiva mail s adrese i imena tvrtke, a ne generičke adrese aplikacije.</li>
</ul>
<div class="callout">
    Predložak poruke prepoznaje tagove <code>{klijent}</code>, <code>{broj}</code>, <code>{datum}</code>,
    <code>{valjanost}</code>, <code>{ukupno}</code>, <code>{tvrtka}</code> i <code>{vlasnik}</code>, koji se
    automatski zamjenjuju stvarnim podacima ponude.
</div>

<h2>3. Iz prihvaćene ponude — račun u jednom koraku</h2>
<p>
    Kad klijent prihvati ponudu, u odjeljku Računi bira se akcija
    <strong>„Kreiraj račun iz ponude"</strong>. Iz padajućeg popisa odabere se ponuda, a aplikacija
    automatski prepiše klijenta, sve stavke (usluge, količine, cijene, rabate i PDV stope),
    napomenu i mjesto izdavanja u novi račun. Ostaje samo provjeriti podatke i izdati račun —
    bez ponovnog unosa, bez rizika da se nešto krivo prepiše.
</p>

<h2>Sve mogućnosti ponuda, na jednom mjestu</h2>
<ul>
    <li>Automatska godišnja numeracija ponuda</li>
    <li>Neograničen broj stavki, s rabatom i PDV stopom po stavci</li>
    <li>Stavke iz kataloga usluga/proizvoda ili ručni unos</li>
    <li>Premještanje i kloniranje stavki u ponudi</li>
    <li>Automatski izračun osnovice, rabata, PDV-a i ukupnog iznosa</li>
    <li>Preuzimanje ponude kao PDF</li>
    <li>Slanje ponude e-mailom s prilagodljivom porukom, CC-om i dodatnim privitcima</li>
    <li>Slanje preko SMTP postavki vlastite tvrtke</li>
    <li>Pretvaranje prihvaćene ponude u račun jednim klikom</li>
    <li>Filtriranje ponuda po godini u popisu</li>
</ul>

<p>
    Rezultat: ponude koje izgledaju profesionalno, stižu do klijenta bez dodatnih koraka, a
    kad posao bude potvrđen — račun je udaljen samo jedan klik.
</p>
