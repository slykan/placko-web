<p>
    Puno korisnika plačko.app-a ne prodaje samo usluge, nego i fizičku robu — pa se prije ili
    kasnije postavi isto pitanje: <strong>koliko toga još imam na skladištu?</strong> Zato radimo
    na novom modulu <strong>Skladište</strong>, koji će se prirodno nadovezati na postojeći katalog
    usluga i proizvoda te na izradu ponuda i računa.
</p>

<div class="callout">
    Modul je trenutno u razvoju — ovo je najava onoga na čemu radimo, ne gotova funkcionalnost.
    Kad bude spreman, javit ćemo se novim postom (i, dakako, e-mailom onima koji ga aktivno traže).
</div>

<h2>Što će skladište donijeti</h2>
<ul>
    <li><strong>Praćenje zalihe po proizvodu</strong> — svaki proizvod iz kataloga dobiva svoju trenutnu količinu na skladištu, umjesto da se to prati u glavi ili u Excelu.</li>
    <li><strong>Automatsko oduzimanje zalihe</strong> — kad izdaš račun ili ponudu s određenom stavkom, količina na skladištu automatski se smanjuje za prodanu količinu.</li>
    <li><strong>Zaprimanje robe (ulazni dokumenti)</strong> — unos primki pri nabavi, koje povećavaju zalihu i bilježe nabavnu cijenu.</li>
    <li><strong>Upozorenja kod niske zalihe</strong> — postaviš minimalnu količinu po proizvodu, a aplikacija te upozori prije nego što ti roba stvarno ponestane.</li>
    <li><strong>Pregled vrijednosti skladišta</strong> — ukupna vrijednost robe na zalihi u svakom trenutku, po nabavnoj i po prodajnoj cijeni.</li>
    <li><strong>Kartica proizvoda</strong> — povijest svih ulaza i izlaza za pojedini artikl, s datumima i dokumentima na koje se odnose.</li>
    <li><strong>Popis (inventura)</strong> — godišnji ili periodični popis stvarnog stanja, s mogućnošću ručne korekcije i evidencijom razlika (manjak/višak).</li>
</ul>

<h2>Što planiramo dodati odmah nakon toga</h2>
<p>
    Skladište je prvi korak prema širem praćenju nabave i troškova:
</p>
<ul>
    <li>Evidencija <strong>dobavljača</strong> i povezivanje primki s dobavljačem</li>
    <li>Podrška za <strong>više skladišta/lokacija</strong>, ako posluješ na više mjesta</li>
    <li>Izvještaji o <strong>prometu zaliha</strong> kroz odabrano razdoblje</li>
    <li>Automatski prijedlog narudžbe kad zaliha padne ispod minimuma</li>
</ul>

<p>
    Cilj je da, kao i kod ponuda i računa, sve ostane jednostavno — bez knjigovodstvenog žargona i
    bez nepotrebnih koraka. Ako prodaješ robu i ovo ti je posebno bitno, javi nam se s prijedlozima
    što bi ti najviše pomoglo — utječe na redoslijed kojim stvari gradimo.
</p>
